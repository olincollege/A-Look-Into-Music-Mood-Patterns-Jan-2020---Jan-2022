def totals(month):
    """
    This function counts the total amount of each type of song for a given month

    Arguments: 
        - month: A dictionary of lists representing the key words in the top ten
                 songs of the month

    Returns:
        - totals: A list representing the total number of each type of song 
    """
    anger_total = 0
    love_total = 0
    joy_total = 0
    sad_total = 0
    angst_total = 0
    
    for key in month:
        list = month[key]
    
        if list[5::].count("sad") > 0:
            sad_total += 1
        elif list[5::].count("love") > 0:
            love_total += 1
        elif list[5::].count("joy") > 0:
            joy_total += 1
        elif list[5::].count("angst") > 0:
            angst_total += 1
        elif list[5::].count("anger") > 0:
            anger_total += 1
        


    return [anger_total, love_total, joy_total, sad_total, angst_total]
